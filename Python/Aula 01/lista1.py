# Listas
# A lista é uma estrutura indexada posicional onde
# nós podemos armazenar multiplos elementos
#                0          1        2       3
minha_lista = ['Nayara', 'Camila', 'Hugo', 'Vitor']

# Acessando Valores
print(minha_lista[0])

# Alterando Valores
print(minha_lista)
minha_lista[3] = 'Pedro'
print(minha_lista)

# Adicionando Valores
minha_lista.append('Marcos')
print(minha_lista)
