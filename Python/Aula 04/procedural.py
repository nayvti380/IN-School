# Paradigma Procedural
# É o paradigma onde nós contruímos nossos programas
# somente de forma altoritmica sem modularizações, uma coisa depois da outra.

# Entrada
n1 = float(input('Digite o primeiro numero: '))
n2 = float(input('Digite o segundo numero: '))

# Processamento
resultado = n1 + n2

# Saída
print(f'O resultado da soma é: {resultado}')
