# Estrutura de Repetição (WHILE)

# Variavel de Controle
contador = 0

while contador < 10:
    print(contador)
    contador += 1 # contador = contador + 1

print('Fim do Programa')

# Quando a contagem começa de 0, e nós comparamos 
# com o sinal de "<" o valor de comparação é a quantidade de repetições