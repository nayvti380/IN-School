contador = 10 # Variavel de Controle

# A variavel de controle é utilizada na
# concição do while
while contador > 0:
    print(contador)

    # Precisamos alterar a variavel de controle 
    # em algum momento durante a repetição
    # de uma forma que a condição do while
    # possa se tornar falsa
    contador -= 1 

print('Fim do Programa.')