# Operadores Relacionais / Comparação

print(2 == 2)            # == | Igualdade
print('Davi' == 'davi')  # == | Igualdade
print('Davi' != 'Pedro') # != | Diferente
print(1 > 4)             # >  | Maior que
print(4 >= 4)            # >= | Maior ou Igual a
print(7 < 7)             # <  | Menor que
print(7 <= 8)            # <= | Menor ou igual a